// ----------------------------------------------------------------------------
// MOptionData
// ----------------------------------------------------------------------------

struct MOptionData
{
   MOptionData(const char *name_, long val)
   {
      name = name_;
      value.n = val;
      isNumeric = true;
   }

   MOptionData(const char *name_, const char *val)
   {
      name = name_;
      value.s = val;
      isNumeric = false;
   }

   const char *name;

   union
   {
      long n;
      const char *s;
   } value;

   bool isNumeric;
};

// ----------------------------------------------------------------------------
// private functions
// ----------------------------------------------------------------------------

// generate a unique id for the next option
static inline int NewMOption()
{
   static size_t s_optionLast = 0;

   return s_optionLast++;
}

